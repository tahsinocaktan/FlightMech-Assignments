
function v = Vnom_cr(H)
v = zeros(size(H));

if (H > 31387)
v = 0.61*((1.4*287.05287*(288.15-10-(0.0065*H))).^(1/2));

else
h1 = (H >= 0) & (H < 2999);

v(h1) = min(220, 150);

h2 = (H >= 3000) & (H < 5999);
v(h2) = min(220, 180);

h3 = (H >= 6000) & (H < 9999);
v(h3) = min(220, 250);

h4 = (H >= 10000) & (H <= 31387);
v(h4) = 220;

end
end