
function v = Vnom_cl(H)
v = zeros(size(H));

if (H > 33516)
v = 0.5*((1.4*287.05287*(288.15-10-(0.0065*H))).^(1/2));

else
h1 = (H >= 0 ) & (H < 499);
v(h1) = (1.3*100)+20;

h2 = (H >= 500 ) & (H < 999);
v(h2) = (1.3*100)+30;

h3 = (H >= 1000 ) & (H < 1499);
v(h3) = (1.3*100)+35;

h4 = (H >= 1500) & (H < 9999);
v(h4) = min(180,250);

h5 = (H >= 10000) & (H <= 33516 );
v(h5) = 170;

end

end
