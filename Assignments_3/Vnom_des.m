
function v = Vnom_des(H)
v = zeros(size(H));

h1 = (H >= 0 ) & (H < 999);
v(h1) = (1.3*90)+5;

h2 = (H >= 1000 ) & (H < 1499);
v(h2) = (1.3*90)+10;

h3 = (H >= 1500 ) & (H < 1999);
v(h3) = (1.3*90)+20;

h4 = (H >= 2000 ) & (H < 2999);
v(h4) = (1.3*90)+50;

h5 = (H >= 3000) & (H < 5999);
v(h5) = min(230,220);

h6 = (H >= 6000) & (H < 9999);
v(h6) = min(230,250);

h7 = (H >= 10000) & (H <= 17424  );
v(h7) = 240;

h8 = H > 17424 ;
v(h8) = 0.5*((1.4*287.05287*(288.15-10-0.0065.*H)).^(1/2));

end